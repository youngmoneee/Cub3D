int dx[] = {1, 0, -1, 0};
int dy[] = {0, 1, 0, -1};
int width = 12;
int height = 12;

int isclosed(char map[][12])
{
    for (int i = 0; i < height; i++)
    {
        for (int j = 0; j < width; j++)
        {
            if (map[i][j] != ' ')
                continue;
            for (int k = 0; k < 4; k++)
               {
                int x = i + dx[k];
                int y = j + dy[k];

                if (x < 0 || x >= height || y < 0 || y >= width)
                    continue;
                if (map[x][y] == '0')
                    return 1;
            }
        }
    }
    return 0;
}

